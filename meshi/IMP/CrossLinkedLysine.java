package meshi.IMP;

public class CrossLinkedLysine {

	private int resNum;
	private double x,y,z;
	
	public CrossLinkedLysine(int resNum, double x, double y, double z) {
		this.resNum = resNum;
		this.x = x;
		this.y = y;
		this.z = z;	
	}

	public int resNum() {
		return resNum;
	}

	public double x() {
		return x;
	}

	public double y() {
		return y;
	}

	public double z() {
		return z;
	}
	

}
